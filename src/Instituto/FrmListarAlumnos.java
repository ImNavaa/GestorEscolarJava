    /**
     * Obtiene la lista de Alumnos y los agrega en la
     * tabla del formulario
     */
    public void listarAlumnos(){
        int cantidadAlumnos = GestionInstituto.listaAlumnos.size();
        DefaultTableModel modelo = (DefaultTableModel) tblAlumnos.getModel();        
        String[] datos = new String[4];
        for(int i=0;i<cantidadAlumnos;i++){
            datos[0]=GestionInstituto.listaAlumnos.get(i).getIdentificacion();
            datos[1]=GestionInstituto.listaAlumnos.get(i).getNombre();
            datos[2]=GestionInstituto.listaAlumnos.get(i).getCorreo();
            datos[3]=GestionInstituto.listaAlumnos.get(i).getGenero();
            modelo.addRow(datos);            
        }      
    }

